package it.polito.latazza;

public class LaTazzaAccount {
	String username;
	String password;
	
	public LaTazzaAccount(String username, String password) {
		this.username = username;
		this.password = password;
	}
	
	public String getUsername() {
		return username;
	}
	
	public String getPassword() {
		return password;
	}
	
	
}
